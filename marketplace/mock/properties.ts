function toMarketplaceProperty(
  p: Property,
  spvMap: Map<string, SPV>
): MarketplaceProperty {
  const targetRaise = p.token_price * p.total_tokens;
  const raisedAmount = p.token_price * p.sold_tokens;
  const fundingProgress =
    p.total_tokens > 0 ? p.sold_tokens / p.total_tokens : 0;
  const spv = spvMap.get(p.id) ?? null;
  return {
    id: p.id,
    slug: p.slug,
    name: p.name,
    description: p.description,
    country: p.location.country,
    city: p.location.city,
    address: p.location.address,
    assetType: p.asset_type,
    status: p.status,
    images: p.images,
    createdAt: p.created_at,
    updatedAt: p.updated_at,
    totalValue: p.total_value,
    tokenPrice: p.token_price,
    totalTokens: p.total_tokens,
    availableTokens: p.available_tokens,
    soldTokens: p.sold_tokens,
    expectedRoi: p.expected_roi,
    rentalYield: p.rental_yield,
    appreciationRate: p.appreciation_rate,
    minInvestment: p.min_investment,
    occupancy: p.occupancy,
    blockchain: p.blockchain,
    contractAddress: p.contract_address,
    tokenId: p.token_id,
    targetRaise,
    raisedAmount,
    fundingProgress,
    availableFractions: p.available_tokens,
    spv,
    hasSpv: spv != null,
  };
}

/** Join SPVs and compute derived display fields. Pure. */
export function getMarketplaceProperties(): MarketplaceProperty[] {
  const spvMap = new Map<string, SPV>();
  for (const s of rawSpvs) spvMap.set(s.property_id, s);
  return rawProperties.map((p) => toMarketplaceProperty(p, spvMap));
}
